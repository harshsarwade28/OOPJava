package Assignment_1;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		  int a = 10, b = 5, sum;

	        sum=a + b; 
	        System.out.println(sum);

	        sum=a - b;
	        System.out.println(sum);

	        sum=a * b; 
	        System.out.println(sum);

	        sum=a / b; 
	        System.out.println(sum);

	        sum=a % b; 
	        System.out.println(sum);
	}
}
